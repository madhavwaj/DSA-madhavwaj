import java.util.Scanner;

// q4. Addition Of Two Numbers
public class q4 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        int sum = 0;
        sum = a+b;
        System.out.println("The addition of two number is "+ sum);
    }
}
